import PptxGenJS from "pptxgenjs";
import type { ComplianceData } from "./types";

const BG = "1a1a2e";
const WHITE = "FFFFFF";
const CYAN = "00d4ff";
const MUTED = "8899aa";
const CARD_BG = "262640";
const GREEN = "22c55e";
const YELLOW = "eab308";
const RED = "ef4444";

function addBgSlide(pptx: PptxGenJS): PptxGenJS.Slide {
  const slide = pptx.addSlide();
  slide.background = { color: BG };
  return slide;
}

export async function generateCompliancePPTX(data: ComplianceData): Promise<Buffer> {
  const pptx = new PptxGenJS();
  pptx.author = "Sentinel";
  pptx.company = "Sentinel – x402 Payment Intelligence";
  pptx.title = `Compliance Report – ${data.teamName}`;
  pptx.layout = "LAYOUT_WIDE";

  slideTitlePage(pptx, data);
  slideExecutiveSummary(pptx, data);
  slidePaymentTrend(pptx, data);
  slideAgentOverview(pptx, data);
  slideTopEndpoints(pptx, data);
  slideNetworkDistribution(pptx, data);
  slideBudgetCompliance(pptx, data);
  slideAlertsIncidents(pptx, data);
  slideRiskAssessment(pptx, data);
  slideReceipts(pptx, data);
  slideRecommendations(pptx, data);
  slideNextSteps(pptx, data);

  const output = await pptx.write({ outputType: "nodebuffer" });
  return output as Buffer;
}

function addSlideTitle(slide: PptxGenJS.Slide, title: string) {
  slide.addText(title, {
    x: 0.5,
    y: 0.3,
    w: 9,
    h: 0.5,
    fontSize: 22,
    color: WHITE,
    fontFace: "Helvetica",
    bold: true,
  });
  slide.addShape("rect" as unknown as PptxGenJS.ShapeType, {
    x: 0.5,
    y: 0.85,
    w: 2,
    h: 0.04,
    fill: { color: CYAN },
  });
}

function addFooter(slide: PptxGenJS.Slide) {
  slide.addText("Generated by Sentinel  |  sentinel.valeocash.com", {
    x: 0.5,
    y: 7.0,
    w: 9,
    h: 0.3,
    fontSize: 8,
    color: MUTED,
    fontFace: "Helvetica",
  });
}

// Slide 1
function slideTitlePage(pptx: PptxGenJS, data: ComplianceData) {
  const slide = addBgSlide(pptx);

  slide.addText("x402 Payment\nCompliance Report", {
    x: 0.8,
    y: 1.5,
    w: 8,
    h: 2,
    fontSize: 36,
    color: WHITE,
    fontFace: "Helvetica",
    bold: true,
    lineSpacing: 42,
  });

  slide.addText(data.teamName, {
    x: 0.8,
    y: 3.8,
    w: 8,
    h: 0.5,
    fontSize: 18,
    color: CYAN,
    fontFace: "Helvetica",
  });

  slide.addText(
    `${fmtDate(data.startDate)} — ${fmtDate(data.endDate)}`,
    { x: 0.8, y: 4.4, w: 8, h: 0.4, fontSize: 14, color: MUTED, fontFace: "Helvetica" }
  );

  slide.addText(
    `Generated: ${new Date(data.generatedAt).toLocaleDateString("en-US", { month: "long", day: "numeric", year: "numeric" })}`,
    { x: 0.8, y: 4.9, w: 8, h: 0.4, fontSize: 11, color: MUTED, fontFace: "Helvetica" }
  );

  addFooter(slide);
}

// Slide 2
function slideExecutiveSummary(pptx: PptxGenJS, data: ComplianceData) {
  const slide = addBgSlide(pptx);
  addSlideTitle(slide, "Executive Summary");

  const s = data.summary;
  const cards = [
    { label: "Total Payments", value: s.total_payments.toLocaleString() },
    { label: "Total Spend", value: `$${s.total_spent_usd.toFixed(2)}` },
    { label: "Active Agents", value: s.active_agents.toString() },
    { label: "Unique Endpoints", value: s.unique_endpoints.toString() },
  ];

  cards.forEach((c, i) => {
    const x = 0.5 + i * 2.4;
    slide.addShape("roundRect" as unknown as PptxGenJS.ShapeType, {
      x,
      y: 1.4,
      w: 2.1,
      h: 1.4,
      fill: { color: CARD_BG },
      rectRadius: 0.1,
    });
    slide.addText(c.value, {
      x,
      y: 1.6,
      w: 2.1,
      h: 0.7,
      fontSize: 24,
      color: CYAN,
      fontFace: "Helvetica",
      bold: true,
      align: "center",
    });
    slide.addText(c.label, {
      x,
      y: 2.3,
      w: 2.1,
      h: 0.4,
      fontSize: 10,
      color: MUTED,
      fontFace: "Helvetica",
      align: "center",
    });
  });

  const moreStats = [
    `Average Payment: $${s.avg_payment_usd.toFixed(4)}`,
    `Maximum Payment: $${s.max_payment_usd.toFixed(4)}`,
    `Failed: ${s.total_failed}  |  Blocked: ${s.total_blocked}`,
  ];

  slide.addText(moreStats.join("\n"), {
    x: 0.5,
    y: 3.3,
    w: 9,
    h: 1.2,
    fontSize: 11,
    color: WHITE,
    fontFace: "Helvetica",
    lineSpacing: 20,
  });

  addFooter(slide);
}

// Slide 3
function slidePaymentTrend(pptx: PptxGenJS, data: ComplianceData) {
  const slide = addBgSlide(pptx);
  addSlideTitle(slide, "Payment Trend");

  if (data.timeseries.length === 0) {
    slide.addText("No payment data available for the selected period.", {
      x: 0.5, y: 2.5, w: 9, h: 1, fontSize: 14, color: MUTED, fontFace: "Helvetica",
    });
    addFooter(slide);
    return;
  }

  const headerOpts: PptxGenJS.TableCellProps = {
    fill: { color: CARD_BG },
    color: CYAN,
    fontSize: 9,
    fontFace: "Helvetica",
    bold: true,
    border: { type: "solid", color: "333355", pt: 0.5 },
    valign: "middle",
  };
  const cellOpts: PptxGenJS.TableCellProps = {
    color: WHITE,
    fontSize: 9,
    fontFace: "Helvetica",
    border: { type: "solid", color: "333355", pt: 0.5 },
    valign: "middle",
  };

  const tableRows: PptxGenJS.TableRow[] = [
    [
      { text: "Date", options: headerOpts },
      { text: "Payments", options: headerOpts },
      { text: "Spend (USD)", options: headerOpts },
    ],
  ];

  const entries = data.timeseries.slice(0, 20);
  for (const t of entries) {
    tableRows.push([
      { text: fmtDate(t.timestamp), options: cellOpts },
      { text: t.count.toString(), options: cellOpts },
      { text: `$${t.total_usd.toFixed(2)}`, options: cellOpts },
    ]);
  }

  slide.addTable(tableRows, { x: 0.5, y: 1.2, w: 6, colW: [2, 2, 2] });
  addFooter(slide);
}

